import random 

r = int(random.randint(-100,100))

found = False


while found == False :
  
  n = int(input("type a number: "))
 
  if n == r :
    found == True
    print("correct number")
  elif n < r :
    print("the number is too low.")
  elif n > r :
    print("the number is too high")
  else:
    print ("invalid number")

